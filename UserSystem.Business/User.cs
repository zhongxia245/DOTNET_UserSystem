		/// <summary>
		/// 获得数据列表
		/// </summary>
		public List<UserSystem.Model.User> DataTableToList(DataTable dt)
		{
			List<UserSystem.Model.User> modelList = new List<UserSystem.Model.User>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				UserSystem.Model.User model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = dal.DataRowToModel(dt.Rows[n]);
					if (model != null)
					{
						modelList.Add(model);
					}
				}
			}
			return modelList;
		}

